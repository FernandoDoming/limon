/*
 * We don't allow "extremely" long paths which we don't expect.
 */

#include <stdio.h>
#include <string.h>
#include <unistd.h>
#include <sys/types.h>
#include <sys/stat.h>
#include <fcntl.h>
#include <pthread.h>

static const char *A255SL = "AAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAA/";

int main()
{
    close(open("/tmp/zipjail-input", O_RDONLY));

    static char longpath[0x10000];

    strcpy(longpath, "/tmp/zipjail-dirtydir/");

    for (int i = 0; i < 10; i++) {
        strcat(longpath, A255SL);
        mkdir(longpath, 0777);
    }

    return 0;
}
